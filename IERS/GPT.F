C Legendre polynomials
      V(1,1) = 1.0D0
      W(1,1) = 0.0D0;
      V(2,1) = z * V(1,1);
      W(2,1) = 0.0;

      DO n=2,nmax
        V(n+1,1) = ((2*n-1) * z * V(n,1) - (n-1) * V(n-1,1)) / n
        W(n+1,1) = 0.0D0
      ENDDO

      DO m=1,nmax
        V(m+1,m+1) = (2*m-1) * (x*V(m,m) - y*W(m,m))
        W(m+1,m+1) = (2*m-1) * (x*W(m,m) + y*V(m,m))
        IF (m < nmax) THEN
          V(m+2,m+1) = (2*m+1) * z * V(m+1,m+1)
          W(m+2,m+1) = (2*m+1) * z * W(m+1,m+1)
        ENDIF
        DO n=m+2,nmax
          V(n+1,m+1) = ((2*n-1)*z*V(n,m+1) - (n+m-1)*V(n-1,m+1)) / (n-m)
          W(n+1,m+1) = ((2*n-1)*z*W(n,m+1) - (n+m-1)*W(n-1,m+1)) / (n-m)
        ENDDO
      ENDDO

C     Geoidal height
      undu = 0.d0
      i = 0
      DO n=0,nmax
        DO m=0,n
          i = i+1
          undu = undu + (a_geoid(i)*V(n+1,m+1) + b_geoid(i)*W(n+1,m+1))
        ENDDO
      ENDDO

C     orthometric height
      hort = dhgt - undu

C     Surface pressure on the geoid
      apm = 0.d0
      apa = 0.d0
      i = 0
      DO n=0,nmax
        DO m=0,n
          i = i+1
          apm = apm + (ap_mean(i)*V(n+1,m+1) + bp_mean(i)*W(n+1,m+1))
          apa = apa + (ap_amp(i) *V(n+1,m+1) + bp_amp(i) *W(n+1,m+1))
        ENDDO
      ENDDO
      pres0  = apm + apa*dcos(doy/365.25d0*2.d0*pi)

C     height correction for pressure
      pres = pres0*(1.d0-0.0000226d0*hort)**5.225d0

C     Surface temperature on the geoid
      atm = 0.d0
      ata = 0.d0
      i = 0
      DO n=0,nmax
        DO m=0,n
          i = i+1
          atm = atm + (at_mean(i)*V(n+1,m+1) + bt_mean(i)*W(n+1,m+1))
          ata = ata + (at_amp(i) *V(n+1,m+1) + bt_amp(i) *W(n+1,m+1))
        ENDDO
      ENDDO
      temp0 =  atm + ata*dcos(doy/365.25d0*2*pi)

C     height correction for temperature
      temp = temp0 - 0.0065d0*hort
      
      END SUBROUTINE
